package questions;

public class CheckNumber {

	public static void main(String[] args) {
		int num = -55;
		if (num == 0)
			System.out.println("Number is zero!");
		else if (num > 0)
			System.out.println(num + " is a positive number!");
		else
			System.out.println(num + " is a negative number!");			
	}

}
